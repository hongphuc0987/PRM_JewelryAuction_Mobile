package com.prm.prm_jewelryauction_mobile.model;

public class RoleModel {
    private int id;
    private String name;

    public RoleModel(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
